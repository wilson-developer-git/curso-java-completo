package aulas.secao_04.aula_20;

public class App {

	public static void main(String[] args) {
		
		int x = 6; 
		int y = 3, 
		result = 0;

		//adição
		result = x + y;
		System.out.println("6 + 3 = "+ result);
		
		//subtração
		result = x - y;
		System.out.println("6 - 3 = "+ result);
		
		//multiplicação
		result = x * y;
		System.out.println("6 * 3 = "+ result);
		
		//divisão
		result = x / y;
		System.out.println("6 / 3 = "+ result);
		
		//mod
		result = x % y;
		System.out.println("6 % 3 = "+ result);
		
	}
	
}
